def hitung_keleliling_segitiga(S_1, S_2, S_3):
    return round((S_1 + S_2 + S_3), 2)

def hitung_luas_selimut(ks,T):
    return round ((ks * T), 2)

def hitung_luas_permukaan(ks,T,a,t):
    return round ((ks * T * a * t), 2)

def hitung_volume(a,t,T):
    return round ((1/2*a*t*T), 2)


